-- Functions
CREATE OR REPLACE FUNCTION public.update_project_space_last_activity()
RETURNS TRIGGER AS $$
BEGIN
    UPDATE public.project_spaces
    SET last_activity_at = now()
    WHERE id = NEW.project_space_id;
    RETURN NEW;
END;
$$ LANGUAGE plpgsql;

CREATE OR REPLACE FUNCTION public.get_user_conversations(p_user_id uuid)
RETURNS TABLE(conversation_id uuid, other_user_id uuid, other_user_username text, last_message_content text, last_message_created_at timestamp with time zone) AS $$
BEGIN
    RETURN QUERY
    SELECT
        c.id AS conversation_id,
        CASE
            WHEN c.user1_id = p_user_id THEN c.user2_id
            ELSE c.user1_id
        END AS other_user_id,
        u.raw_user_meta_data->>'username' AS other_user_username,
        (SELECT content FROM public.messages m WHERE m.conversation_id = c.id ORDER BY m.created_at DESC LIMIT 1) AS last_message_content,
        (SELECT created_at FROM public.messages m WHERE m.conversation_id = c.id ORDER BY m.created_at DESC LIMIT 1) AS last_message_created_at
    FROM
        public.conversations c
    JOIN
        auth.users u ON u.id = (CASE WHEN c.user1_id = p_user_id THEN c.user2_id ELSE c.user1_id END)
    WHERE
        c.user1_id = p_user_id OR c.user2_id = p_user_id
    ORDER BY
        (SELECT created_at FROM public.messages m WHERE m.conversation_id = c.id ORDER BY m.created_at DESC LIMIT 1) DESC;
END; 
$$ LANGUAGE plpgsql;

CREATE OR REPLACE FUNCTION public.create_project_space_and_category(p_name text, p_description text, p_category_name text, p_project_space_type public.project_space_type)
RETURNS uuid AS $$
DECLARE
    v_category_id uuid;
    v_project_space_id uuid;
BEGIN
    -- Upsert category
    INSERT INTO public.project_space_categories (name) VALUES (p_category_name) ON CONFLICT (name) DO UPDATE SET name = p_category_name RETURNING id INTO v_category_id;
    
    -- Insert project_space
    INSERT INTO public.project_spaces (name, description, creator_id, category_id, project_space_type) 
    VALUES (p_name, p_description, auth.uid(), v_category_id, p_project_space_type)
    RETURNING id INTO v_project_space_id;
    
    RETURN v_project_space_id;
END;
$$ LANGUAGE plpgsql;

create function public.handle_new_user()
returns trigger as $$
begin
  insert into public.profiles (id, username, avatar_url)
  values (new.id, new.raw_user_meta_data->>'username', new.raw_user_meta_data->>'avatar_url');
  return new;
end;
$$ language plpgsql security definer;

-- Triggers
CREATE TRIGGER on_new_message_update_project_space_activity
AFTER INSERT ON public.project_space_messages
FOR EACH ROW
EXECUTE FUNCTION public.update_project_space_last_activity();

create trigger on_auth_user_created
  after insert on auth.users
  for each row execute procedure public.handle_new_user();

